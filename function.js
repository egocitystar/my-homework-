// An arrow function is  a concise way to write.
// functions in javaScript,

// Welcome function
const user = (name) =>
    `welcome to my website, ${name}`;
console.log(user("martinz."));

// sum of 3 numbers
const sum = (a, b, c) => a + b + c;
console.log(sum(5, 10, 15));

// modulus of 2 numbers
const modulus = (a, b) => a % b;
console.log(modulus(11, 3));




